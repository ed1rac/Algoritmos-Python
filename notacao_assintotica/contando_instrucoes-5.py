# -*- encoding: utf-8 -*-
"""
exemplo 5 - while
============================================
inicialização do i -> 1 instrução
comparacoes com o max -> 1 + 10 instrucoes
incremento do i --> 10 instruções
impressao --> 10 instruções
"""
max = 10                    # --> 1
for i in range(0,max+1):    # --> 1 + 1 + 10 + 10
    print(i)                # --> 10 
# 33 instrucoes no total
